package com.peterwang.toys.mapdraw;

/**
 * 位置点操作类
 *
 * @author peter_wang
 * @create-time 15/11/4 16:28
 */
public class ST_DPoint {
    public double x =0.0;
    public double y =0.0;

    public double GetX()
    {
        return x;
    }

    public void SetX(double value)
    {
        x=value;
    }

    public double GetY()
    {
        return y;
    }

    public void SetY(double value)
    {
        y=value;
    }

}  
